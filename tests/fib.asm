ROM_START=0xF000
RESET_VECTOR=0xFFFC

*=ROM_START

LDA #10

; Inputs through A
FIB:
SBC #2
STA 0xFC ; Store A in 0xFD
LDA #0
STA 0xFD ; Prev prev
LDA #1
STA 0xFE ; Prev
LDA 0xFC
FIBLOOP:
LDA 0xFD
ADC 0xFE
STA 0xFF ; Cur
LDA 0xFE
STA 0xFD
LDA 0xFF
STA 0xFE
DEC 0xFC
BNE FIBLOOP
LDA 0xFF

*=RESET_VECTOR
!word ROM_START
